export function createPostTemplate(index: number, sectorId: string, sectorName: string): CivilProtectionPost {
  const base = cpPostNames[index % cpPostNames.length];
  return {
    id: `cp-post-${sectorId}`,
    name: `${base} — ${sectorName}`,
    sectorId,
    officers: 18 + (index % 5) * 4,
    discipline: 54 + (index % 4) * 6,
    brutality: 36 + (index % 5) * 7,
    corruption: 24 + (index % 6) * 6,
    morale: 50 + (index % 3) * 8,
    lambdaInfluence: 8 + (index % 4) * 4,
    rationLeakage: 8 + (index % 5) * 5,
    abuseReports: 4 + (index % 4) * 3,
    falseCharges: 5 + (index % 3) * 4,
    arrestsToday: 0,
    seizedRations: 0,
    compromisedOfficers: index % 4 === 0 ? 1 : 0,
    lastIncident: 'Aucun incident majeur transmis au registre COAN.',
  };
}
